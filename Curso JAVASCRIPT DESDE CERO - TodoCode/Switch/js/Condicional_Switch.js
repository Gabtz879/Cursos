var color = prompt("Ingresa un color: ");

switch(color.toLowerCase()){
    case "rojo":
        alert("Color rojo");
        break;
    case "azul":
        alert("Color Azul");
        break;
    case "amarillo":
        alert("Color amarillo");
        break;
    case "verde":
        alert("Color verde")
        break;
    default:
        alert("Color no valido")
}